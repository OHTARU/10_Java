
public class Ex02_gt_lt {

	public static void main(String[] args) {
		
		// 비교연산자 : > , < 
		// 비교연산자 결과 : true , false
		
		int num1 = 100;
		int num2 = 200;
		
		// num1 > num2 ?
		System.out.println(num1>num2);
		System.out.println(num1 + ">" + num2 + ":" + (num1>num2));
		
		System.out.println(num1 == num2);
		System.out.println(num1 != num2);
		
	}

}
